# underscores in numbers
# 12000300 
# 12_000_300

print(12000300) # this is printed as 12000300
print(12_000_300) # this is printed as 12000300, and easy to write code as we can use the underscore

# scientific notation numbers
print(0.000000000000000000000000003) # this is printed as 3e-27

# octsl number, starts with 0O or 0o
print(0o123) # this is printed as 83

# hexadecimal number, starts with 0X or 0x
print(0x123) # this is printed as 291

